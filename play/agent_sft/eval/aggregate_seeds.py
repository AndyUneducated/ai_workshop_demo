"""跨 seed 聚合 evals/runs/index.jsonl → markdown 报告（pandas + tabulate 实现）.

输入：[`play/evals/runs/index.jsonl`](../../evals/runs/index.jsonl)（append-only event log）
过滤：按 `--tasks`（默认 4 个 baseline task）+ `--since` ISO date / `--last N runs`
分组：(task, model_label_w/o_seed) — `@seed=K` 后缀剥掉
聚合：每数值指标 mean / std / count（嵌套 dict 由 `pd.json_normalize` 自动 dot-path 展开）
输出：markdown 表，每 task 一节，列=模型 (n=K)，行=指标 mean ± std

用法：
    python play/agent_sft/eval/aggregate_seeds.py
    python play/agent_sft/eval/aggregate_seeds.py --since 2026-05-09
    python play/agent_sft/eval/aggregate_seeds.py --tasks nudge_fire_rate,bfcl_slice
    python play/agent_sft/eval/aggregate_seeds.py --output baselines/qwen2.5-7b-vs-32b.md
"""

from __future__ import annotations

import argparse
import re
import sys
from pathlib import Path

import pandas as pd
from tabulate import tabulate

HERE = Path(__file__).resolve().parent
PLAY_DIR = HERE.parent.parent
INDEX_PATH = PLAY_DIR / "evals" / "runs" / "index.jsonl"
DEFAULT_OUTPUT = HERE / "baselines" / "qwen2.5-7b-vs-32b.md"
DEFAULT_TASKS = ["nudge_fire_rate", "agent_traj", "bfcl_slice", "mmlu_slice"]

SEED_SUFFIX_RE = re.compile(r"@seed=\d+$")
N_RUNS_COL = ("__n_runs__", "")


def _strip_seed_suffix(model_label: str) -> str:
    """`ollama:qwen2.5:7b@seed=42` → `ollama:qwen2.5:7b`（group key）."""
    return SEED_SUFFIX_RE.sub("", model_label)


def load_index(path: Path) -> pd.DataFrame:
    """读 jsonl → DataFrame；不存在则 SystemExit 提示."""
    if not path.exists():
        sys.exit(f"index not found: {path}\n  hint: run `python play/agent_sft/eval/run_baseline.py` first")
    return pd.read_json(path, lines=True)


def filter_runs(
    df: pd.DataFrame, *, tasks: list[str], since: str | None, last_n: int | None
) -> pd.DataFrame:
    """task ∈ tasks ∧ mode == 'run' ∧ created_at >= since ∧ tail(last_n)."""
    out = df[df["task"].isin(tasks) & (df["mode"] == "run")]
    if since:
        out = out[out["created_at"] >= since]
    if last_n is not None:
        out = out.sort_values("created_at").tail(last_n)
    return out


def aggregate(df: pd.DataFrame) -> pd.DataFrame:
    """group by (task, model_clean) → 每数值指标 mean/std/count；嵌套 dict 自动 dot-path 展开.

    返回 DataFrame：
      - index: MultiIndex(task, model_clean)
      - columns: MultiIndex(metric_path, stat ∈ {mean, std, count})
      - 额外列 ('__n_runs__', '')：本组总 run 数（与各 metric 的 count 可能不同）.
    """
    if df.empty:
        return pd.DataFrame()

    df = df.copy()
    df["model_clean"] = df["model"].map(_strip_seed_suffix)

    metrics = pd.json_normalize(df["aggregated"]).set_index(df.index)
    numeric_cols = metrics.select_dtypes(include="number").columns.tolist()

    keys = df[["task", "model_clean"]].reset_index(drop=True)
    vals = metrics[numeric_cols].reset_index(drop=True)
    wide = pd.concat([keys, vals], axis=1)

    grouped = wide.groupby(["task", "model_clean"], sort=True)
    if numeric_cols:
        agg = grouped[numeric_cols].agg(["mean", "std", "count"])
    else:
        agg = pd.DataFrame(index=grouped.size().index)
        agg.columns = pd.MultiIndex.from_tuples([], names=[None, None])
    agg[N_RUNS_COL] = grouped.size()
    return agg


def render_markdown(agg: pd.DataFrame) -> str:
    """每 task 一节，列=模型 (n=K)，行=指标；多 seed 显示 `mean ± std`，单 seed 仅 mean."""
    lines = ["# Baseline aggregation (multi-seed mean ± std)", ""]
    lines.append("Generated by [`aggregate_seeds.py`](../aggregate_seeds.py) from `play/evals/runs/index.jsonl`.")
    lines.append("")

    if agg.empty:
        lines.append("_(no data)_")
        lines.append("")
        return "\n".join(lines)

    all_metric_cols = sorted({c[0] for c in agg.columns if c[0] != N_RUNS_COL[0]})

    for task in agg.index.get_level_values("task").unique():
        sub = agg.xs(task, level="task")  # index = model_clean
        models = list(sub.index)
        n_runs = {m: int(sub.loc[m, N_RUNS_COL]) for m in models}

        # 只列出本 task 下至少一行 count>0 的 metric——避免跨 task 污染（bfcl 表
        # 不应出现 mmlu 的 accuracy_by_subject 占位 "—"）
        task_metrics = [
            m for m in all_metric_cols
            if (m, "count") in sub.columns and (sub[(m, "count")] > 0).any()
        ]

        lines.append(f"## `{task}`")
        lines.append("")

        if not task_metrics:
            lines.append("_(no aggregated metrics found)_")
            lines.append("")
            continue

        rows = []
        for metric in task_metrics:
            row = [f"`{metric}`"]
            for m in models:
                cnt = sub.loc[m, (metric, "count")] if (metric, "count") in sub.columns else 0
                mean = sub.loc[m, (metric, "mean")] if (metric, "mean") in sub.columns else None
                std = sub.loc[m, (metric, "std")] if (metric, "std") in sub.columns else None
                if cnt == 0 or pd.isna(mean):
                    row.append("—")
                elif cnt == 1:
                    row.append(f"{mean:.4f}")
                else:
                    row.append(f"{mean:.4f} ± {std:.4f}")
            rows.append(row)

        headers = ["metric"] + [f"`{m}` (n={n_runs[m]})" for m in models]
        # disable_numparse: 保留我们已格式化好的 "0.9000"，避免 tabulate 把它再 parse 回 0.9
        lines.append(tabulate(rows, headers=headers, tablefmt="github", disable_numparse=True))
        lines.append("")

    lines.append("## Method note")
    lines.append("")
    lines.append(
        "样本标准差（pandas 默认 ddof=1）；单 seed run 显示 mean only；多 seed run 显示 mean ± std. "
        "嵌套 metric（如 `accuracy_by_subject.philosophy`）由 `pd.json_normalize` 自动按 dot-path 展开."
    )
    lines.append("")
    return "\n".join(lines)


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__.split("\n", 1)[0])
    parser.add_argument(
        "--tasks",
        default=",".join(DEFAULT_TASKS),
        help=f"comma-separated task names (default: {','.join(DEFAULT_TASKS)})",
    )
    parser.add_argument("--since", default=None, help="ISO date filter, e.g. 2026-05-09")
    parser.add_argument(
        "--last-n", type=int, default=None,
        help="only consider last N runs (after task / since filter)",
    )
    parser.add_argument(
        "--index", type=Path, default=INDEX_PATH,
        help=f"index.jsonl path (default: {INDEX_PATH})",
    )
    parser.add_argument(
        "--output", type=Path, default=DEFAULT_OUTPUT,
        help=f"markdown report path (default: {DEFAULT_OUTPUT})",
    )
    args = parser.parse_args(argv)

    df = load_index(args.index)
    filtered = filter_runs(df, tasks=args.tasks.split(","), since=args.since, last_n=args.last_n)
    print(f"loaded {len(df)} index rows; {len(filtered)} match filter", file=sys.stderr)
    if filtered.empty:
        sys.exit("no rows match filter; nothing to aggregate")

    agg = aggregate(filtered)
    md = render_markdown(agg)
    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(md, encoding="utf-8")
    print(f"wrote → {args.output}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main())
